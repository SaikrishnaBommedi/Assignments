def additon(num1,num2):
    return num1+num2

def sub(num1,num2):
    return num1-num2 

def mul(num1,num2):
    return num1*num2

def div(num1,num2):
    return num1/num2 

num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))
print("Addition of 2 numbers is ",additon(num1,num2))
print("subtraction of 2 numbers is ",sub(num1,num2))
print("multiplication of 2 numbers is ",mul(num1,num2))
print("division of 2 numbers is ",div(num1,num2))